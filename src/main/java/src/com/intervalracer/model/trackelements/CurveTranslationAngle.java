package com.intervalracer.model.trackelements;

/**
 * Enumeration for the translation angles that are allowed for the
 * TrackElements. A track element kan have an angle of 0, 90, 180 or 270
 * degrees.
 */
public enum CurveTranslationAngle {

	BASE_ANGLE,

	ANGLE_90,

	ANGLE_180,

	ANGLE_270;
}
